struct PGPTestSet {
    fileprivate static var ALL_TEST_SETS: [String: Self] = [:] // swiftlint:disable:this strict_fileprivate

    let publicKey: String
    let privateKey: String
    let fingerprint: String
    let passphrase: String

    fileprivate func collect() -> Self { // swiftlint:disable:this strict_fileprivate
        Self.ALL_TEST_SETS[fingerprint] = self
        return self
    }
}

struct MultiKeyPGPTestSet {
    let publicKeys: String
    let privateKeys: String
    let fingerprints: [String]
    let passphrases: [String]
}

